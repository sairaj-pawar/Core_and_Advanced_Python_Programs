''' Assignment 2 - Declare a square() function with one parameter.
Then call the function and pass one number and display the square of that number. '''

# Declaring the square() function
def square(num):
    sqr = num * num  # Calculating the square of the number
    return sqr  # Returning the result

# Calling the function and storing the result
result = square(5)

# Printing the square of the number
print("The square of the number is:", result)

'''Output'''

'''The square of the number is: 25'''
