'''Assignment 1 - Write a Python program to convert a list to a tuple. 

Input: listx = [5, 10, 7, 4, 15, 3] 

Output: (5, 10, 7, 4, 15, 3)'''


# Given list
listx = [5, 10, 7, 4, 15, 3]

# Convert list to tuple
tuplex = tuple(listx)

# Print result
print("Tuple:", tuplex)  

'''
### Output ###
Tuple: (5, 10, 7, 4, 15, 3)
'''
