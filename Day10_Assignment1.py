#Assignment 1 - Write a Python program to sum all the items in a list.
# Define a list of numbers
numbers = [10, 20, 30, 40, 50]

# Initialize sum variable
total = 0  

# Loop through each item in the list and add it to total
for num in numbers:
    total += num  

# Print the sum
print("The sum of all items in the list is:", total)

''' 
Output:
The sum of all items in the list is: 150
'''
