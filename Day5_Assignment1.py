'''Assignment 1 =  Declare a div() function with two parameters.
Then call the function and pass two numbers and display their division.'''

# Define a function div() that takes two parameters
def div(a, b):
    div = a / b  # Perform division of a by b
    return div  # Return the division result

# Call the function with two numbers (20 and 40)
result = div(20, 40)

# Print the division result
print("The division of two numbers:", result)

''' Output '''

'''The division of two numbers: 0.5'''